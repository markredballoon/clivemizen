function _get_project_info( $what, $info ) {
	$info = explode('<!--more-->', $info);

	if( $what == '0' )
		return $info[0];

	if( $what == '1' )
		return $info[1];

	if( $what == '2' )
		return $info[2];

	if( $what == '3' )
		return $info[3];

	if( $what == '4' )
		return $info[4];

	if( $what == '5' )
		return $info[5];

	if( $what == '6' )
		return $info[6];

	if( $what == '7' )
		return $info[7];

	return '';

}

